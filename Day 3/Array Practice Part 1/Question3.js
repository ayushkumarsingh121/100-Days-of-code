// ### **3. Add an Element at the End**

// **Question:**

// Write a program to add "Grapes" to the end of the array `["Apple", "Banana"]` and print the array.

// **Expected Output Example:**
// ["Apple", "Banana", "Grapes"]

let fruits = ["Apple", "Banana"];
console.log("Old fruit list", fruits)

fruits.push("Grapes")
console.log("Updated fruit list", fruits)
