// ### **5. Add an Element at the Beginning**

// **Question:**

// Write a program to add "Tomato" at the start of the array `["Carrot", "Potato"]` and print the updated array.

// **Expected Output Example:**

// ["Tomato", "Carrot", "Potato"]


let vegitables = ["Carrot", "Potato"];
console.log("Old vegitable lists", vegitables)

vegitables.unshift("Tomato")
console.log("Updated vegitable lists", vegitables)
