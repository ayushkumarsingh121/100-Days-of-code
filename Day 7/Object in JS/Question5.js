// ### **5. Updating an Object Property**

// **Question:**

// Given the object `user = { name: "Ayush", age: 19 }`, update the `age` to `20` and log the updated object.

// **Expected Output:**

// `{ name: "Ayush", age: 20 }`


let user = {
  name: "Ayush", 
  age: 19 
}

 user.age = 20
 console.log(user)