// ### **8. Using Object.values()**

// **Question:**

// Given the object `employee = { name: "John", position: "Manager" }`, use `Object.values()` to log the values of the object.

// **Expected Output:**

// `["John", "Manager"]`


let employee = { 
  name: "John", 
  position: "Manager" 
}

console.log(Object.values(employee))