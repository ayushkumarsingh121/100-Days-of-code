// ### **3. Accessing Properties with Bracket Notation**

// **Question:**

// Given the object `person = { "first name": "John", "last name": "Doe" }`, access and log the `first name` property using bracket notation.

// **Expected Output:**

// `John`


let person = { 
  "first name": "John", 
  "last name": "Doe" 
}

console.log(person["first name"])