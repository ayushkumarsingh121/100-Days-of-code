// ### **2. Accessing Properties with Dot Notation**

// **Question:**

// Given the object `car = { brand: "Tesla", model: "Model S" }`, access and log the `brand` property using dot notation.

// **Expected Output:**

// `Tesla`


let car = { 
  brand: "Tesla", 
  model: "Model S"
}

console.log(car.brand) // Tesla